package com.mkalugin.basecamp;

public class BasecampAuthenticationException extends BasecampException {

    private static final long serialVersionUID = 1L;

    public BasecampAuthenticationException() {
        super();
    }

    public BasecampAuthenticationException(String message, Throwable cause) {
        super(message, cause);
    }

    public BasecampAuthenticationException(String message) {
        super(message);
    }

    public BasecampAuthenticationException(Throwable cause) {
        super(cause);
    }
    
}
