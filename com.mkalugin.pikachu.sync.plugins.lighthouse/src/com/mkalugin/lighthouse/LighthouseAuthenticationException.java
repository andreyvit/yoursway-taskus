package com.mkalugin.lighthouse;

public class LighthouseAuthenticationException extends LighthouseException {

    private static final long serialVersionUID = 1L;

    public LighthouseAuthenticationException() {
        super();
    }

    public LighthouseAuthenticationException(String message, Throwable cause) {
        super(message, cause);
    }

    public LighthouseAuthenticationException(String message) {
        super(message);
    }

    public LighthouseAuthenticationException(Throwable cause) {
        super(cause);
    }
    
}
